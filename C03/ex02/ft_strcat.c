char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		destlen;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	destlen = i;
	i = 0;
	while (src[i] != '\0')
	{
		dest[destlen + i] = src[i];
		i++;
	}
	dest[destlen + i] = '\0';
	return (dest);
}
