#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	i = 2;
	if (nb <= 1 || (nb != 2 && nb % 2 == 0)
		|| (nb != 3 && nb % 3 == 0))
		return (0);
	while (i < nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (nb < 2147483647)
	{
		if (ft_is_prime(nb))
			return (nb);
		++nb;
	}
	return (0);
}

