int	ft_count_if(char **tab, int length, int (*f)(char *))
{
	int	c;
	int	a;

	a = 0;
	c = 0;
	while (tab[c][0] != '\0')
	{
		if (f(tab[c]))
			a++;
		c++;
	}
	return (a);
}
