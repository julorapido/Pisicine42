char	*ft_strlowcase(char *str)
{
	int		i;
	int		c;

	i = 0;
	while (str[i] != '\0')
	{
		c = (int) str[i];
		if (c >= 65 && c <= 90)
		{
			c += 32;
		}
		str[i] = c;
		i++;
	}
	return (str);
}
